# Задача 2. Долги
# В базе данных «МирПрогБанка» есть список должников. Операторам банка дали задание позвонить каждому пятому человеку из списка (нумерация начинается с нуля) и уточнить, какую сумму каждый из них задолжал банку.

# Что нужно сделать
# Напишите программу, которая получает данные о количестве должников, а затем спрашивает у каждого пятого (начиная с нуля) его долг. В конце выводится общая сумма долгов.

# Пример 1

# Введите количество должников: 13
# Должник с номером 0
# Сколько должны? 1000
# Должник с номером 5
# Сколько должны? 5000
# Должник с номером 10
# Сколько должны? 2000

# Общая сумма долга: 8000

# Пример 2

# Введите количество должников: 10
# Должник с номером 0
# Сколько должны? 1000
# Должник с номером 5
# Сколько должны? 5000

# Общая сумма долга: 6000

# Что оценивается
# Результат вывода соответствует условию.
# В выводе присутствует общая сумма долга.
# Формат вывода соответствует примеру (не выведены числа без описания).
# Для решения использован цикл for и range c шагом.

# ===================================================================================================================================

n = int(input("Введите количество должников: "))
total_debt = 0

# Перебираем каждого пятого должника начиная с 0
for i in range(0, n, 5):
    print(f"Должник с номером {i}")
    debt = int(input("Сколько должны? "))
    total_debt += debt

print(f"\nОбщая сумма долга: {total_debt}")

# ======================================================================================================================================

# Объяснение:

# Ввод количества должников: Пользователь вводит число n — общее количество должников.

# Инициализация суммы долга: total_debt хранит накопленную сумму.

# Цикл с шагом 5:

# range(0, n, 5) генерирует последовательность: 0, 5, 10, ... до n-1 (включительно).

# Для каждого индекса в этой последовательности запрашивается сумма долга.

# Расчёт общей суммы: Каждая введённая сумма добавляется к total_debt.

# Вывод результата: После завершения цикла выводится общая сумма.