# Напишите программу-игру, в которой один человек загадывает число от 1 до 10, а другой пытается его угадать. Программа должна запрашивать число у пользователя до тех пор, пока тот не угадает загаданное. После каждой попытки выводите подсказки, больше или меньше загаданного введённое число. Также после отгадки выводите количество попыток.

# Выводите сообщения в соответствии с примером.
# Пример (загадали число 7)

# Введите число: 3
# Число меньше, чем нужно. Попробуйте ещё раз!
# Введите число: 10
# Число больше, чем нужно. Попробуйте ещё раз!
# Введите число: 8
# Число больше, чем нужно. Попробуйте ещё раз!
# Введите число: 7
# Вы угадали! Число попыток: 4
# Что оценивается
# Формат вывода соответствует заданию.


import random

secret_number = random.randint(1, 10)
attempts = 0

print("Загадано число от 1 до 10. Попробуйте угадать!")

while True:
    guess = int(input("Введите число: "))
    attempts += 1

    if guess == secret_number:
        print(f"Вы угадали! Число попыток: {attempts}")
        break
    elif guess < secret_number:
        print("Число меньше, чем нужно. Попробуйте ещё раз!")
    else:
        print("Число больше, чем нужно. Попробуйте ещё раз!")


# Объяснение работы программы:

# Генерация числа: random.randint(1, 10) создает случайное целое от 1 до 10.

# Счетчик попыток: Переменная attempts считает количество введенных чисел.

# Бесконечный цикл: Запрашивает числа до тех пор, пока не будет угадано.

# Проверка догадки:

# Если число угадано — выводится поздравление и количество попыток.

# Если число меньше/больше — дается подсказка.
