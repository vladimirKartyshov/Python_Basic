# Используется флаг для учёта факта звонка жены.


total_tasks = 0
called_wife = False

print("Начался восьмичасовой рабочий день.")

for hour in range(1, 9):
    print(f"{hour}-й час")

    # Ввод количества решенных задач
    tasks = int(input("Сколько задач решит Максим? "))
    total_tasks += tasks

    # Проверка звонка жены
    if not called_wife:
        answer = int(input("Звонит жена. Взять трубку? (1 — да, 0 — нет): "))
        if answer == 1:
            called_wife = True
    # Если уже ответил, пропускаем вопрос о звонке

print(f"\nРабочий день закончился. Всего выполнено задач: {total_tasks}")
if called_wife:
    print("Нужно зайти в магазин.")


