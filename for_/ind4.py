# Задача 4. Отрезок
# Что нужно сделать
# Напишите программу, которая считывает с клавиатуры два числа: a и b, считает и выводит в консоль среднее арифметическое всех чисел из отрезка [a; b], кратных числу 3.

# Подсказки:

# Средним арифметическим нескольких чисел называют сумму этих чисел, делённую на их количество.
# Кратность означает, что число делится на заданное без остатка.
# Пример

# Начало отрезка: -5
# Конец отрезка: 12
# Числа из отрезка, которые делятся на 3:
# -3
# 0
# 3
# 6
# 9
# 12

# Среднее арифметическое этих чисел: 4.5

# Что оценивается
# Результат вывода соответствует условию.
# В выводе присутствует описание результата.
# Для решения используется конструкция for.

# =======================================================================================================================

a = int(input("Начало отрезка: "))
b = int(input("Конец отрезка: "))

start = min(a, b)
end = max(a, b)

divisible_numbers = []

# Собираем числа, кратные 3, через цикл for
for num in range(start, end + 1):
    if num % 3 == 0:
        divisible_numbers.append(num)

# Выводим числа и среднее арифметическое
print("Числа из отрезка, которые делятся на 3:")
for num in divisible_numbers:
    print(num)

average = sum(divisible_numbers) / len(divisible_numbers)
print(f"Среднее арифметическое этих чисел: {average:.1f}")

# =======================================================================================================================

# Пояснение:

# Определение границ отрезка: start и end вычисляются как минимальное и максимальное из введённых значений, чтобы корректно обработать любой порядок ввода (например, a=5, b=3).

# Сбор чисел кратных 3: Через цикл for проверяем каждое число на отрезке. Если число делится на 3 без остатка, добавляем его в список divisible_numbers.

# Вывод чисел: Каждое найденное число выводится на новой строке.

# Расчёт среднего: Сумма чисел делится на их количество. Результат форматируется до одного знака после запятой.