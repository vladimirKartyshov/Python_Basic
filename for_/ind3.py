# Ввод количества учеников: Программа запрашивает число n — количество учеников.

# Инициализация счётчиков: count_3, count_4, count_5 хранят количество троек, четвёрок и пятёрок.

# Цикл для подсчёта оценок:

# Для каждого ученика вводится оценка.

# В зависимости от оценки увеличивается соответствующий счётчик.

# Сравнение результатов:

# Если троек больше, чем четвёрок и пятёрок — выводится сообщение о троечниках.

# Если четвёрок больше пятёрок — выводится сообщение о хорошистах.

# В остальных случаях (когда пятёрок больше) — сообщение об отличниках.
