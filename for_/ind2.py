# Задача 2. Посчитай чужую зарплату...
# Бухгалтер устала постоянно считать вручную среднегодовую зарплату сотрудников компании и, чтобы облегчить себе жизнь, обратилась к программисту.

# Что нужно сделать
# Напишите программу, которая принимает от пользователя зарплату сотрудника за каждый из 12 месяцев и выводит на экран среднюю зарплату за год.

# Пример
# Введите зарплату сотрудника: 20000
# Введите зарплату сотрудника: 40000
# Введите зарплату сотрудника: 35000
# Введите зарплату сотрудника: 40000
# ....
# ....
# ....
# Средняя зарплата за год: 37916.6

# Что оценивается
# Результат вывода соответствует условию.
# Вывод содержит описание результата (не выведено одно число без описания).
# Задача решена с помощью конструкции for.


# ================================================================================================================================

total = 0.0  # Сумма зарплат за год

# Запрашиваем зарплату для каждого из 12 месяцев
for month in range(12):
    salary = float(input("Введите зарплату сотрудника: "))
    total += salary

# Вычисляем среднее значение и округляем до одного знака после запятой
average = total / 12
print(f"Средняя зарплата за год: {average:.1f}")

#===========================================================================================================================


# Пояснение:

# Суммирование зарплат: Переменная total накапливает сумму всех введённых зарплат.

# Цикл for: Повторяется 12 раз (по количеству месяцев).

# Преобразование ввода: Введённое значение преобразуется в float, чтобы учесть дробные числа.

# Расчёт среднего: Средняя зарплата вычисляется как total / 12.

# Форматированный вывод: Результат выводится с одним знаком после запятой (.1f).